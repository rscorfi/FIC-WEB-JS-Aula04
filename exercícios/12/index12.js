/* 12. Dado um círculo com raio r = 7 , calcule sua área e 
circunferência. Fórmulas:

Área: A = π * r2
Circunferência: C = 2 * π * r
Considere π = 3.14 . */

let raio = 7
let pi = 3.14
let area = pi * raio ** 2
let circunferência = 2 * pi * raio

console.log(`Área do circulo: ${area}`)
console.log(`Circunferência do círculo: ${circunferência}`)